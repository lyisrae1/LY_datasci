# str function

# This function compactly displays the the internal structure of an R object
# str is an alternative to using the summary()
# well suited for compactly displaying the contents of lists (even lists that are compacted)

# example 1

x <- rnorm(100, 2, 4)
summary(x)
#    Min. 1st Qu.  Median    Mean 3rd Qu.    Max. 
# -8.4942 -0.9575  1.6312  1.8887  5.0448 10.2068

# summarizing x only gives you the first five examples of the vector

str(x)
#  num [1:100] -3.13 5.78 -4.6 3.84 2.67 ...

# str() provided more information than the summary function.
# It told you that x is a numeric vector that has 100 elements; then gave the first five examples

# example 2

